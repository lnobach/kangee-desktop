package de.roo.ui.swing.tray;

/**
 * 
 * @author Leo Nobach (dev@getkangee.com)
 *
 */
public class RooTrayException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4560755834128516321L;

	public RooTrayException() {
		super();
	}

	public RooTrayException(String message, Throwable cause) {
		super(message, cause);
	}

	public RooTrayException(String message) {
		super(message);
	}

	public RooTrayException(Throwable cause) {
		super(cause);
	}

}
